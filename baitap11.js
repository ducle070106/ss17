console.log(Math.abs(-5.5));  // Kết quả: 5.5 (|-5.5| = 5.5)
console.log(Math.abs(10));    // Kết quả: 10 (|10| = 10)
console.log(Math.ceil(4.4));  // Kết quả: 5 (4.4 làm tròn lên thành 5)
console.log(Math.ceil(5.8));  // Kết quả: 6 (5.8 làm tròn lên thành 6)
console.log(Math.floor(5.7)); // Kết quả: 5 (5.7 làm tròn xuống thành 5)
console.log(Math.floor(6.1)); // Kết quả: 6 (6.1 làm tròn xuống thành 6)
console.log(Math.round(10.1)); // Kết quả: 10 (10.1 gần 10 hơn 11 => làm tròn xuống 10)
console.log(Math.round(20.6)); // Kết quả: 21 (20.6 gần 21 hơn 20 => làm tròn lên 21)